    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = DiseaseJTable.getSelectedRow();

        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a disease to update.");
            return;
        }
        else{
            Disease disease = (Disease) DiseaseJTable.getValueAt(selectedRow,0);
            UpdateDiseaseJPanel panel = new UpdateDiseaseJPanel(userProcessContainer, disease);
            userProcessContainer.add("UpdateDiseaseJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();;
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

