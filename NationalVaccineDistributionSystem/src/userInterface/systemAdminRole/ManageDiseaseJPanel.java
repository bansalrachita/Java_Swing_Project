    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) DiseaseJTable.getModel();
        dtm.setRowCount(0);
        for(Disease disease:diseaseList){
            Object[] row = new Object[3];
            row[0] = disease;
            row[1] = disease.getName();
            row[2] = disease.getDescription();
            dtm.addRow(row);
        }
    }
