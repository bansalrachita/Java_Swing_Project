    private void btnAddVaccineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddVaccineActionPerformed
        String name = txtName.getText();
        Vaccine.Type stock = (Vaccine.Type) cmbStock.getSelectedItem();
        
        if(name.equals("") || stock.equals("")){
            JOptionPane.showMessageDialog(null,"Please enter valid values");
            return;
        }
        else{
            
            System.out.println("listsize" + vaccineCatalog.getVaccineList().size());
            if(vaccineCatalog.getVaccineList().size()>0 ){
                for(Vaccine v: vaccineCatalog.getVaccineList()){
                    if(v.getName().equals(name)){
                        JOptionPane.showMessageDialog(null,"This vaccine already exists in the stock");
                        return;
                    }
                }
            }
        }
        vaccine = vaccineCatalog.createVaccine(name);
        txtCode.setText(vaccine.getCode());
        txtCreatedTime.setText(time());
       // vaccine.setName(name);
        vaccine.setDateCreated(txtCreatedTime.getText());
        vaccine.setStock(stock);
        vaccine.setLastUpdated(time());
        JOptionPane.showMessageDialog(null,"Vaccine Added successfully");
        btnAddVaccine.setEnabled(false);
        txtName.setEnabled(false);
        cmbStock.setEnabled(false);
        return;
    }//GEN-LAST:event_btnAddVaccineActionPerformed


