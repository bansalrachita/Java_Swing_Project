    private void populatecmbStateNetworks(){
        cmbState.removeAllItems();
        for(Network states: network.getNetWorkList()){
            cmbState.addItem(states.getState());
        }
    }
    private void btnAddPHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPHDActionPerformed
         String state = (String) cmbState.getSelectedItem();
        if(network.getEnterpriseDirectory().getEnterpriseList().isEmpty()){
             JOptionPane.showMessageDialog(null,"No centers found. Cannot create a PHD for this Network");
             return;
         }
        else if(txtPHDName.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please give a valid name" );
            return;
        }
         else{
            for(Network stateNetwork : network.getNetWorkList()){
                if(stateNetwork.getState().equals(state)){
                    EnterpriseDirectory enterpriseDirectory = stateNetwork.getEnterpriseDirectory();
                    if(enterpriseDirectory.getEnterpriseList().isEmpty()){
                    enterpriseDirectory.createAndAddEnterprise(txtPHDName.getText(), Enterprise.EnterpriseType.PHD);
                    JOptionPane.showMessageDialog(null,"Public Health Department Added successfully");
                    txtPHDName.setEnabled(false);
                    btnAddPHD.setEnabled(false);
                    cmbState.setEnabled(false);
                    return;
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Public HealthDepartment already created.");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null,"No network exists.Please go to Manage Networks to create a network.");
                }

            }
         }
    }//GEN-LAST:event_btnAddPHDActionPerformed


