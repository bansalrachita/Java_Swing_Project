    private void populateRequestTable() {
        DefaultTableModel dtm = (DefaultTableModel) RequestStatusJTable.getModel();
        dtm.setRowCount(0);
        
        WorkRequest req = (WorkRequest)request;
        if(req != null){
            Object[] row = new Object[4];
            if(req instanceof ClinicShipmentWorkRequest){
                ClinicShipmentWorkRequest shipmentReq = (ClinicShipmentWorkRequest)req;
            
            row[0] = shipmentReq.getOrderRequest();
            row[1] = shipmentReq.getRequestDate();
            row[2] = shipmentReq.getStatus();
            row[3] = shipmentReq.getReceiver() == null ? "": req.getReceiver();
            }
            else if(req instanceof ProviderWorkRequest){
                ProviderWorkRequest providerReq = (ProviderWorkRequest)req;
            
            row[0] = providerReq.getOrderRequest();
            row[1] = providerReq.getRequestDate();
            row[2] = providerReq.getStatus();
            row[3] = providerReq.getReceiver() == null ? "": req.getReceiver();
            }
            dtm.addRow(row);
        }
        
    }

