    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
try{
        String name = txtManufacturer.getText();
        String street = txtStreet.getText();
        String city = txtCity.getText();
        String state = txtState.getText();
        String Country = txtCountry.getText();
        String zip = txtZip.getText();
        
        for(Organization o: enterprise.getOrganizationDirectory().getOrganizationList()){
                if(o.getType().equals(Organization.Type.Manfacturer)){
                    if(o.getName().equals(name)){
                        JOptionPane.showMessageDialog(null,"The manufacturer already exists.");
                        return;
                    }
                }
        }
        if(name.equals("") || state.equals("")|| city.equals("")||Country.equals("")||zip.equals("") || street.equals("")){
            JOptionPane.showMessageDialog(null,"Please enter valid values.");
            return;
        }
        else{
                        organization=enterprise.getOrganizationDirectory().createOrganization(Organization.Type.Manfacturer,name);
                        organization.setType(Organization.Type.Manfacturer);
                        manufacturerOrg = (ManufacturerOrganization) organization;
                        //Manufacturer manufacturer = manufacturerOrg.getManufacturerDirectory().addManufacturer();
                        Address address = manufacturerOrg.getAddress();
                        address.setCountry(Country);
                        address.setState(state);
                        address.setStreet(street);
                        int Zip = Integer.parseInt(zip);
                        address.setZip(Zip);
                        btnAdd.setEnabled(false);
                        txtManufacturer.setEnabled(false);
                        txtCity.setEnabled(false);
                        txtState.setEnabled(false);
                        txtCountry.setEnabled(false);
                        txtZip.setEnabled(false);
                        txtStreet.setEnabled(false);
                        JOptionPane.showMessageDialog(null,"Manufacturer added successfully.");
                    
                
            
            

        }
}
catch(Exception e){
    JOptionPane.showMessageDialog(null,"Please enter correct values");
    enterprise.getOrganizationDirectory().getOrganizationList().remove(organization);
    manufacturerOrg.getManufacturerDirectory().getManufacturerList().remove(manufacturer);
    return;
}

    }//GEN-LAST:event_btnAddActionPerformed

